package drawingObjects;

/**
 * the object for all the joints on the ends of members and between members
 * 
 * @author blake M.
 * 
 * @version 1.0 May 21, 2025
 */
public class Joint {

	/**
	 * x cordanit
	 */
	public double x;
	/**
	 * y cordanit
	 */
	public double y;

	/**
	 * Constructor: Creates a joint
	 * 
	 * @param x x cordanit
	 * @param y y cordanit
	 */
	public Joint(double x, double y) {
		this.x = x;
		this.y = y;
	}

}
